// 1. Number
let age = 23;
console.log(typeof age);
console.log(age);

// 2. String
let user = "Gabriela";
console.log(typeof user);
console.log(user);

// 3. Boolean
let isStudent = true;
console.log(typeof isStudent);
console.log(isStudent);

// 4. Undefined
let city;
console.log(typeof city);
console.log(city);

// 5. Null
let car = null;
console.log(typeof car);
console.log(car)

// 6. Array
let colors = ["red", "green", "blue"];
console.log(typeof colors);
console.log(colors)

// 7. Object
let person = {
    firstName: "Gabriela",
    age: 23
};
console.log(typeof person);
console.log(person)

// 8. Function
function sayHello() {
    return "Hello!";
}
console.log(typeof sayHello);
console.log(sayHello)

